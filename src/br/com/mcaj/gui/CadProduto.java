    public void Gravar() {
        System.out.println(txtNome.getText());
        System.out.println(txtPreco.getText());
        System.out.println(txtQuantidade.getText());

        DaoCadProduto dao = new DaoCadProduto();
        BeanProduto bean = new BeanProduto();

        bean.setName(txtNome.getText());
        bean.setPrice(Double.parseDouble(txtPreco.getText()));
        bean.setQuantity(Integer.parseInt(txtQuantidade.getText()));

        dao.inserir(bean);
    }

    public void Atualizar() {
        System.out.println(txtNome.getText());
        System.out.println(txtPreco.getText());
        System.out.println(txtQuantidade.getText());

        DaoCadProduto dao = new DaoCadProduto();
        BeanProduto bean = new BeanProduto();

        bean.setName(txtNome.getText());
        bean.setPrice(Double.parseDouble(txtPreco.getText()));
        bean.setQuantity(Integer.parseInt(txtQuantidade.getText()));

        dao.atualizar(bean);
    }

    public void deletar() {
        System.out.println(txtNome.getText());
        System.out.println(txtPreco.getText());
        System.out.println(txtQuantidade.getText());

        DaoCadProduto dao = new DaoCadProduto();
        BeanProduto bean = new BeanProduto();

        bean.setName(txtNome.getText());
        dao.deletar(bean);
    }

